# create empty dict
'''
time comlexity O(1)
space comlexity O(1)
'''
my_dict = dict()
print(my_dict)

my_dict2 = dict()
print(my_dict2)

#create dict with data
'''
time comlexity O(N)
space comlexity O(N)
'''

eng_sp = dict(one='uno',two='dos',three='tres')
print(eng_sp)

eng_sp2 = {'one':'uno','two':'dos','three':'tres'}
print(eng_sp2)

eng_sp3 = [('one','uno'),('two','dos'),('three','tres')]
print(dict(eng_sp3))

